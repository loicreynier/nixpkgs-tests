def add(a, b):
    """Addition of two numbers

    Parameters
    ----------
    a : int or float
        The first number to add.
    b : int or float
        The second number to add.

    Returns
    -------
    int or float
        The sum of `a` and `b`

    Examples
    --------
    >>> add(2, 3)
    5
    >>> add(3.14, 1)
    4.14
    """
    return a + b
